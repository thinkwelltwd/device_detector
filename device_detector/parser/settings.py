def normalized_name(name: str, abbreviations: dict, names: dict) -> str:
    """
    Lookup Brand name by lower casing to account
    for spelling differences.
    """
    try:
        abbrev = abbreviations[str(name).strip().lower()]
        return names[abbrev]
    except KeyError:
        pass
    return name


